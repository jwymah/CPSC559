package com.github.group;

public class Command {

    /**
     * Command constructor.
     */
    public Command () {
    }

    /**
     * Always returns the integer one.
     *
     * @return 1
     */
    public int getOne () {
        return 1;
    }

    /**
     * Always returns the string "Hello."
     *
     * @return "Hello."
     */
    public String getHello () {
        return "Hello.";
    }

}
